#include "string.h"
#include "stdint.h"

/**
 *
 */
size_t strcspn(const char *str, const char *chrs)
{
	size_t ret = 0;
	while (*str)
	{
		if (strchr(chrs, *str)) return ret;
		str++;
		ret++;
	}
	return ret;
}
